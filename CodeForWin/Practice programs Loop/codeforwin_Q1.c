/*
Print all natural numbers from 1 to n.(use for, while, do-while loop)

*/
#include <stdio.h>
#include <stdlib.h>

int main()
{
    int i, end;
    printf("\nEnter a number for end: ");
    scanf("\n%d", &end);
 


   for (; i <= end ;)
    {
        printf("\n%d",i);
        i = i + 1;
    }
 
    i = 1;
    do
    {
        printf("\n%d",i);
    }while (i ++ , i <= end);
 


    i = 1;
    while (i <= end)
    {
        printf("\n%d",i);
        i = i + 1;
    }
	
return 0;
}
